public class Loops {
  public static void main(String a[]){
    int i = 4;

    while(i<=4){
      System.out.println(i);
      i++;
    }
    System.out.println("Bye "+ i);

    do {
      System.out.println(i);
      i++;
    }while(i<=4);

    for(int j=0; j<=i; j++){
      System.out.println(j);

    }
    
    System.out.println(i);
  
  
  }
}
